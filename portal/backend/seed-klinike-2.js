function downloadFile(url, destPath) {
  return new Promise((resolve, reject) => {
    const proto = url.startsWith("https") ? https : http;
    const file = fs.createWriteStream(destPath);
    proto.get(url, (res) => {
      if (res.statusCode === 301 || res.statusCode === 302) {
        file.close();
        fs.unlink(destPath, () => {});
        return downloadFile(res.headers.location, destPath).then(resolve).catch(reject);
      }
      if (res.statusCode !== 200) {
        file.close();
        fs.unlink(destPath, () => {});
        return reject(new Error(`HTTP ${res.statusCode} za ${url}`));
      }
      res.pipe(file);
      file.on("finish", () => file.close(resolve));
      file.on("error", (err) => { fs.unlink(destPath, () => {}); reject(err); });
    }).on("error", reject);
  });
}

async function run() {
  await mongoose.connect(process.env.MONGO_URI || "mongodb://localhost:27017/portal");

  let inserted = 0, imgOk = 0, imgErr = 0;
  const cityCount = {};

  for (let i = 0; i < klinike.length; i++) {
    const k = { ...klinike[i] };
    const imgUrl = k._img;
    delete k._img;

    const city = k.address.split(",").pop().trim().replace(/^\d{5}\s*/, "").trim();
    cityCount[city] = (cityCount[city] || 0) + 1;

    process.stdout.write(`[${i + 1}/${klinike.length}] ${k.companyName}... `);

    if (imgUrl) {
      const ext = imgUrl.includes(".jpeg") ? "jpg" : "jpg";
      const filename = `seed2-${Date.now()}-${i}.${ext}`;
      const destPath = path.join(UPLOADS_DIR, filename);
      try {
        await downloadFile(imgUrl, destPath);
        k.imageUrl = `/uploads/${filename}`;
        imgOk++;
        console.log("slika OK, ubačeno.");
      } catch (err) {
        imgErr++;
        console.log(`slika GREŠKA (${err.message}), ubačeno.`);
      }
    }

    k.order = 1000 + i;
    await Post.create(k);
    inserted++;
  }

  console.log(`\n✓ Ubačeno: ${inserted} klinika`);
  console.log(`✓ Slike preuzete: ${imgOk}, greške: ${imgErr}`);
  console.log("\nRaspored po gradovima:");
  Object.entries(cityCount).sort((a, b) => b[1] - a[1]).forEach(([city, count]) => {
    console.log(`  ${city}: ${count} ustanova`);
  });

  await mongoose.disconnect();
}

run().catch(console.error);
