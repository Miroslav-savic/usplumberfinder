function downloadImage(url, dest) {
  return new Promise((resolve, reject) => {
    const proto = url.startsWith("https") ? https : http;
    const file = fs.createWriteStream(dest);
    proto.get(url, (res) => {
      if (res.statusCode === 301 || res.statusCode === 302) {
        file.close();
        fs.unlink(dest, () => {});
        return downloadImage(res.headers.location, dest).then(resolve).catch(reject);
      }
      res.pipe(file);
      file.on("finish", () => file.close(resolve));
    }).on("error", (err) => {
      fs.unlink(dest, () => {});
      reject(err);
    });
  });
}

async function run() {
  await mongoose.connect(process.env.MONGO_URI || "mongodb://localhost:27017/hospitals");
  console.log("Connected. Seeding 100 US clinics...\n");

  for (let i = 0; i < clinics.length; i++) {
    const clinic = clinics[i];
    const imgUrl = images[i % images.length];
    let imageUrl = "";

    try {
      const fname = `us-${Date.now()}-${Math.random().toString(36).slice(2)}.jpg`;
      const dest = path.join(UPLOADS_DIR, fname);
      await downloadImage(imgUrl, dest);
      imageUrl = `/uploads/${fname}`;
    } catch (e) {
      console.warn(`  ⚠ Image failed: ${e.message}`);
    }

    const post = new Post({ ...clinic, imageUrl, order: 0 });
    await post.save();
    console.log(`  ✅ [${i + 1}/100] ${clinic.companyName}`);
  }

  console.log("\nDone. 100 US clinics inserted.");
  await mongoose.disconnect();
}

run().catch(console.error);
